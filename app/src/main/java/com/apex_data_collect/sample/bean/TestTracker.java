package com.apex_data_collect.sample.bean;

/**
 * @author SteelCabbage
 * @date 2018/12/12
 */
public class TestTracker {
    private String date;
    private String name;
    private String age;
    private String work;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getWork() {
        return work;
    }

    public void setWork(String work) {
        this.work = work;
    }
}
